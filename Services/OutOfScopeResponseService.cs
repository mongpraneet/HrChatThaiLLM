พิมพ์คำถาม HR ได้เลย เช่น "วันลาคงเหลือเท่าไหร่" หรือ "เบิกค่ารักษาปี 2568"
""";
    }

    public string BuildOutOfScopeResponse(string gender = "Male")
    {
        // ✅ คำนวณคำลงท้ายและสรรพนามตามเพศ
        string politeWord = IsFemaleGender(gender) ? "ค่ะ" : "ครับ";
        string pronoun = IsFemaleGender(gender) ? "ดิฉัน" : "ผม";

        string template = OutOfScopeResponses[Random.Shared.Next(OutOfScopeResponses.Length)];

        // ✅ ส่งค่าเข้าไป Map ทั้ง {0} (คำลงท้าย) และ {1} (สรรพนาม)
        return string.Format(template, politeWord, pronoun);
    }

    private static bool IsFemaleGender(string? gender)
    {
        if (string.IsNullOrWhiteSpace(gender)) return false;
        var value = gender.Trim();
        return value.Equals("F", StringComparison.OrdinalIgnoreCase)
               || value.Equals("Female", StringComparison.OrdinalIgnoreCase);
    }
