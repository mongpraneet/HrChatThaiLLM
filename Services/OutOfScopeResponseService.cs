        string pronoun = IsFemaleGender(gender) ? "ดิฉัน" : "ผม";

        return $"""
{pronoun}คือ HR Chat AI ผู้ช่วยงานทรัพยากรบุคคลภายในองค์กร{politeWord}

{pronoun}ช่วยได้ในเรื่องหลักๆ เช่น:
- วันลาคงเหลือและประวัติการลา
- เวลาเข้าออกงานและข้อมูลลงเวลา
- สิทธิ/ประวัติค่ารักษาพยาบาล
- ข้อมูลพนักงานและข้อมูลหน่วยงานที่เกี่ยวข้อง
- กราฟเข้าออกงาน และ กราฟการเบิกค่ารักษา
- ข้อมูลแผนกและฝ่ายที่เกี่ยวข้อง
- ข้อมูลเกี่ยวกับบริษัทและฝ่ายบุคคล
