    public string BuildResponse(GenderPreference preference)
    {
        var responses = preference switch
        {
            GenderPreference.Female => FemaleResponses,
            GenderPreference.LGBT => FemaleResponses,
            GenderPreference.Male => MaleResponses,
            _ => NeutralResponses
        };
        return responses[Random.Shared.Next(responses.Length)];
    }
